package java_practice;

public class Java100_for_Iteration001 {
    public static void main(String[] args) {
        // for문
        for( int i=0; i<10; i++) {
            // 1. i 초기값 0으로 세팅
            // 2. 조건 확인: 10보다 작은지
            // 3. 조건에 맞으면 {} 내부 코드 실행
            // 4. 마지막에 i 값에 1 더함
            System.out.print(i+" ");
        }
        System.out.println();
    }
}
