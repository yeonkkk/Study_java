package variable;

public class Java100_variable_001 {
    public static void main(String[] args){

        int a;  //--- 정수가 저장될 변수 a 선언--;;
        int b;
        int sum;

        a = 3;
        b = 4;
        sum = a + b;

        System.out.println(a);
        System.out.println(b);
        System.out.println(sum);

        sum = a + b + a;
        System.out.println(sum);

    }
}
