package java_practice.variable;

public class Java100_variable_DataType3 {
    public static void main(String[] args) {
        
        // 1
        short a = 'A';
        System.out.println(a);      // 아스키 테이블 값 출력 (65)   

        // 2
        short b = 90;
        System.out.println((char)b); // 아스키 테이블 값 출력 (Z)

        // 3
        char c = 'a';
        System.out.println((short)c); // 아스키 테이블 값 출력 (97)

    }
}
